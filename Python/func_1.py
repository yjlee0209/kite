# list 선언
nums = [123, 234, 54, 254, 435]

# 최대값 반환 함수 : max
max_val = max(nums)
print('최대값 >>>', max_val)

# 최소값 반환 함수 : min
min_val = min(nums)
print('최소값 >>>', min_val)

# list 요소들의 합을 반환 함수 : sum
sum_val = sum(nums)
print('총합 >>>', sum_val)

# list 요소들의 평균값
ave_val = sum_val / len(nums)
print('평균 >>>', ave_val)